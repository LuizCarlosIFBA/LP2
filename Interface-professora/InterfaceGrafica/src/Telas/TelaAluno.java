    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        // TODO add your handling code here:
        if (camposValidos()){
        Aluno a1;
        int mat = Integer.parseInt(textMatricula.getText());
        int turno;
        if (opcaoMatutino.isSelected())
            turno = 1;
        else if (opcaoVespertino.isSelected())
            turno = 2;
        else
            turno=3;
        a1 = new Aluno(textNome.getText(),mat,opcaoCurso.getSelectedItem().toString(),turno );
        listaAluno.add(a1);
        JOptionPane.showMessageDialog(null, "Aluno incluído com sucesso!");
        limpar();
        }
        
    }//GEN-LAST:event_botaoSalvarActionPerformed
    public boolean camposValidos(){
        if (textMatricula.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Informe a matricula");
            return false;
        }
        else if (textNome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Informe o nome do aluno");
            return false;
        }
        else if (opcaoCurso.getSelectedIndex() < 0){
            JOptionPane.showMessageDialog(null, "Informe o curso");
            return false;
        }
        else
            return true;
            
            
            
    }
    private void botaoPesquisar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisar1ActionPerformed
        // TODO add your handling code here:
        int mat = Integer.parseInt(textMatricula.getText());
        for (Aluno alu: listaAluno){
            if (alu.getMatricula()==mat){
                textNome.setText(alu.getNome());
                if (alu.getTurno()==1)
                    opcaoMatutino.setSelected(true);
                else if (alu.getTurno()==2)
                    opcaoVespertino.setSelected(true);
                else
                    opcaoNoturno.setSelected(true);
                if (alu.getCurso().equals("ADM"))
                    opcaoCurso.setSelectedIndex(0);
                else if (alu.getCurso().equals("Direito"))
                    opcaoCurso.setSelectedIndex(1);
                else if (alu.getCurso().equals("Medicina"))
                    opcaoCurso.setSelectedIndex(2);
                else
                    opcaoCurso.setSelectedIndex(3);
            }
        }
    }//GEN-LAST:event_botaoPesquisar1ActionPerformed

