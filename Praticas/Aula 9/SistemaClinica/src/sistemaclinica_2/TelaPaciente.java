    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Paciente pac;
        int cpf;
        String bairro;
        char sexo;
        cpf = Integer.parseInt(campoCPF.getText());
        bairro = campoBairro.getModel().getSelectedItem().toString();
        if(opcaoMasculino.isSelected())
            sexo = 'M';
        else
            sexo = 'F';

        pac = new Paciente(cpf,campoNome.getText(),campoEndereco.getText(),campoTelefone.getText(),bairro,sexo);
        
        listaPac.add(pac);
        
        JOptionPane.showMessageDialog(null, "Incluido com sucesso!");
        limpar();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int cpf;
        cpf = Integer.parseInt(campoBuscaCPF.getText());
        
        for (int i=0;i<listaPac.size();i++)
            if (listaPac.get(i).getCpf()==cpf)
            {   
                int cpfCampo = listaPac.get(i).getCpf();
                campoCPF.setText(String.valueOf(cpfCampo));
                campoNome.setText(listaPac.get(i).getNome());
                campoEndereco.setText(listaPac.get(i).getEndereco());
                campoTelefone.setText(listaPac.get(i).getTelefone());
                if (listaPac.get(i).getSexo()=='M')
                    opcaoMasculino.setSelected(true);
                else
                    opcaoFeminino.setSelected(true);
                campoBairro.setSelectedItem(listaPac.get(i).getEndereco());

            }
        
        
      
    }//GEN-LAST:event_jButton2ActionPerformed

    
    public void limpar(){
        campoCPF.setText("");
        campoBuscaCPF.setText("");
        campoNome.setText("");
        campoBairro.setSelectedIndex(0);
        campoEndereco.setText("");
        campoTelefone.setText("");
        opcaoMasculino.setSelected(true);
    }
